package Class7;

public class ForLoops3 {
    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            System.out.println(i);

            /*
            when you know exactly the amount of times you want to loop
            through a block of code, use "for loop" instead of "while loop".
            when we don't know how many times we need to repeat a block of code
            we should be going with "while loop" most of the time it happens when
            the user input is involved.
             */

        }
    }
}
